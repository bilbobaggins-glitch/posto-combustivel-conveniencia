    private void vendaCadastrarBtActionPerformedOrFail(java.awt.event.ActionEvent evt) throws ConexaoException, ExcecaoComposta {                                                 
        if(gerenciador.clientesBuscarTodos() == null || gerenciador.clientesBuscarTodos().size() < 1){
            ExcecaoComposta exComp = new ExcecaoComposta();
            exComp.excecaoAdicionar(new NaoHaClientesCadastrados());
            
            throw exComp;
        }
        trocarPainel(new VendaPainel(gerenciador));
    }
    
