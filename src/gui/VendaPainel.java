    private void calcularBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularBtActionPerformed
        // TODO add your handling code here:
        gerenciador.vendaNovaSetCliente((Cliente) clienteCmbBx.getSelectedItem());
        gerenciador.vendaNovaSetData(new Date());
        gerenciador.vendaNovaSetDias(Integer.parseInt(diasTxtFld.getText()));
        if(aVistaBt.isSelected()){
            gerenciador.vendaNovaSetPagamentoForma("avista");
        }else{
            gerenciador.vendaNovaSetPagamentoForma("aprazo");
        }
        gerenciador.vendaNovaCalcular();
        subtotalTxt.setText(Double.toString(gerenciador.vendaNovaGetSubtotal()));
        totalTxt.setText(Double.toString(gerenciador.vendaNovaGetTotal()));
        
    }//GEN-LAST:event_calcularBtActionPerformed

