    private void atualizarTabela(){
        DefaultTableModel model = (DefaultTableModel) vendaProdutoTbl.getModel();
        model.setRowCount(0);
 
        for(VendaProduto vendaProduto : gerenciador.getVendaNovaVendaProdutos()){
            model.addRow(new Object[]{
                    vendaProduto.getProduto().getNome(),
                    vendaProduto.getProduto().getPrecoAtual(),
                    vendaProduto.getQuantidade(),
                    vendaProduto.getQuantidade() * vendaProduto.getProduto().getPrecoAtual()
            });
        }
    }

